export function updatePressTactile(data) {
    plotlock = true;
    i+=1;

    let press = (parseFloat(data[2])+parseFloat(data[5])+
        parseFloat(data[8])+parseFloat(data[11]))/200;
    if(list_i.length>=30){
        pressData.shift();
        list_i.shift();
    }
    list_i.push(i);
    pressData.push(press);
    plotlock = false;
}
